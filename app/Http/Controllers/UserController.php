    public function logar(Request $request){

        request()->validate([
        'email' => 'required',
        'password' => 'required',
        ]);
 
        $user = $request->only('email', 'password');
        if (Auth::attempt($user)) {
           return redirect()->intended('/feed');
        }
        return Redirect::to("/feed");
    }   

